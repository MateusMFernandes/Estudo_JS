/*ABS = A função retorna o valor absoluto de um numero, significa que essa expressão sempre retornara um valor positivo do negativo convertido para positio

Exemplo de uso:
    var valor1 = Math.abs (-1234); Resultado = 1234
*/

/* CEIL: Este método retorna um inteiro maior ou igual a um número. O resultado deste método é equivalente ao arredondamento de um número.
Arredondando tanto negtivos como positivos
Exemplo de uso
        var valor1 = Math.ceil (14.6); Resultado: 15
*/

/*POW (base, expoente): Este método retorna a base elevada à potência do expoente. Por exemplo, 2 elevado a décima potência é 1024. 
Exemplo de uso:
    var valor1 = Math.pow ( 1024,2);
*/

/*RANDOM: Este método retorna um número aleatório entre 0 e 1 com até 15 dígitos. Este número aleatório é definido através do relógio do computador.
Exemplo de uso:
    alert (Math.random ());
 */

/*ROUND: Com este método é possível arredondar um valor. O arredondamento segue a regra de arredondamento que vimos anteriormente.
Exemplo de uso:
    var valor1 = Math.round (121.6); resultado: 122
*/

/*SQRT: Retorna a raíz quadrada de um número.
Exemplo de uso:
    var valor1 = Math.sqrt (9); Resultado: 3
 */